    private void tblUserDetailsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblUserDetailsMouseClicked
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) tblUserDetails.getModel();
        int selectedIndex = tblUserDetails.getSelectedRow();
        
        String userTypeValue;
        int userTypeIndex = 0;

        txtForename.setText(model.getValueAt(selectedIndex, 1).toString());
        txtSurname.setText(model.getValueAt(selectedIndex, 2).toString());
        txtUsername.setText(model.getValueAt(selectedIndex, 3).toString());
        txtPassword.setText(model.getValueAt(selectedIndex, 4).toString());
        userTypeValue = model.getValueAt(selectedIndex, 5).toString();
        txtEmail.setText(model.getValueAt(selectedIndex, 6).toString());
        txtPhoneNumber.setText(model.getValueAt(selectedIndex, 7).toString());

        switch (userTypeValue) {
            case "Student":
                userTypeIndex = 0;
                break;
            case "Job Seeker":
                userTypeIndex = 1;
                break;
            case "Tourist":
                userTypeIndex = 2;
                break;
            case "Business":
                userTypeIndex = 3;
                break;
        }
        cbxUserType.setSelectedIndex(userTypeIndex);
    }//GEN-LAST:event_tblUserDetailsMouseClicked

